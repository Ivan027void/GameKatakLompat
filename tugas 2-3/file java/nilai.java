
/**
 * Abstract class nilai - berisi method untuk memberi nilai dan mengubah nilai
 *
 * @author Ivan Chiari
 * @version 1.1
 */
public abstract class nilai
{
    /**
     * method mengembalikan nilai
     *
     * @return nilai
     */
   
    public abstract int getNilai();
    
    /**
     * method mengubah nilai
     *
     * @param nilai
     * @return nilai baru
     */
    public abstract void setNilai(int nilai);

}
